// Задача 1: Проверка возраста для пенсии
// Если возраст меньше 65 – "Вам ещё рано на пенсию"
// Если 65 или больше – "Поздравляем с пенсионным возрастом!"
let age = parseInt(prompt("Задача 1: Введите ваш возраст:"), 10);
if (age < 65) {
  console.log("Вам ещё рано на пенсию");
  alert("Вам ещё рано на пенсию");
} else {
  console.log("Поздравляем с пенсионным возрастом!");
  alert("Поздравляем с пенсионным возрастом!");
}

// Задача 2: Сравнение чисел
// Вводим два числа и сравниваем их:
// "Первое число больше", если первое больше;
// "Второе число больше", если второе больше;
// "Числа равны", если они равны.
let num1 = parseFloat(prompt("Задача 2: Введите первое число:"));
let num2 = parseFloat(prompt("Задача 2: Введите второе число:"));
if (num1 > num2) {
  console.log("Первое число больше");
  alert("Первое число больше");
} else if (num2 > num1) {
  console.log("Второе число больше");
  alert("Второе число больше");
} else {
  console.log("Числа равны");
  alert("Числа равны");
}

// Задача 3: Определение чётности
// Вводим число и проверяем его на чётность.
// Если число делится на 2 без остатка – "Это чётное число"
// Иначе – "Это нечётное число"
let num = parseInt(prompt("Задача 3: Введите число:"), 10);
if (num % 2 === 0) {
  console.log("Это чётное число");
  alert("Это чётное число");
} else {
  console.log("Это нечётное число");
  alert("Это нечётное число");
}